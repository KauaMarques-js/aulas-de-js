let c = 1 //seta um valor para c
while (c <= 10){ //enquanto c for menor ou igual a 10 ele repeti o while
    console.log(`Passo ${c}`) 
    c++ //adiciona mais 1 valor pro c (mesma coisa de c + 1 ate passar 10 para ser false)
}    //c muda de valor ate chegar e parar a reéticao


let a = 1 
do { //faça esse bloco, ate o while ser falso
    console.log(`Passo ${a}`)
    a++ //adiciona mais 1
} while(a <= 10) //enquanto for true volta para o do, se false continue o codigo
//uma forma diferente onde faz o bloco primeiro

